export const getDuasByCategory = (categoryId: string): Dua[] => {
  return duas.filter(dua => dua.category === categoryId);
};

export const searchDuas = (query: string): Dua[] => {
  const lowercaseQuery = query.toLowerCase();
  return duas.filter(dua => 
    dua.title.toLowerCase().includes(lowercaseQuery) ||
    dua.bangla.toLowerCase().includes(lowercaseQuery) ||
    (dua.transliteration && dua.transliteration.toLowerCase().includes(lowercaseQuery))
  );
};
