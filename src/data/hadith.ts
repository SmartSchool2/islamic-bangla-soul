export const getHadithsByCategory = (categoryId: string): Hadith[] => {
  return hadiths.filter(hadith => hadith.category === categoryId);
};

export const getDailyHadith = (): Hadith => {
  const today = new Date();
  const index = today.getDate() % hadiths.length;
  return hadiths[index];
};

export const searchHadiths = (query: string): Hadith[] => {
  const lowercaseQuery = query.toLowerCase();
  return hadiths.filter(hadith => 
    hadith.title.toLowerCase().includes(lowercaseQuery) ||
    hadith.bangla.toLowerCase().includes(lowercaseQuery)
  );
};
